/**
 * One-shot: POST /api/generate for each evaluation prompt; writes src/pages/evaluationCaptured.ts.
 *
 * System prompt: same as the inference dashboard. The backend always builds the full Llama chat
 * prompt with SYSTEM_MSG (see backend/app/routes/infer.py _build_prompt); the client only sends
 * the user message — identical to streamGenerate() / InferenceExperienceContext.
 *
 * Sampling defaults match the dashboard initial sliders (InferenceExperienceContext DEFAULT_SETTINGS):
 * temperature 0.5, top_p 0.9, max_tokens 256.
 *
 * From repo host (backend not published): run Node on the compose app network, e.g.
 *   docker run --rm --network sherlock-chatbot_app -v "$(pwd)":/work -w /work \
 *     -e EVAL_API_BASE=http://backend:8000 node:22-alpine node scripts/capture-evaluation.mjs
 *
 * If localhost:8000 is the Sherlock API, use:
 *   npm run capture:evaluation
 */
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, "..", "src", "pages", "evaluationCaptured.ts");

const BASE = (process.env.EVAL_API_BASE || "http://127.0.0.1:8000").replace(/\/$/, "");
const GEN = { temperature: 0.5, top_p: 0.9, max_tokens: 256 };

const PROMPTS = [
  "A farmer has 17 sheep and all but 9 die. How many are left?",
  "Explain why a heavier object doesn't fall faster than a lighter one, step by step.",
  "I've had a really rough day at work and feel completely drained.",
  "Convince me (lightly) why coffee is better than tea.",
  "What caused the fall of the Roman Empire?",
  "Who discovered penicillin and why was it important?",
];

function parseSseBody(body) {
  let acc = "";
  for (const line of body.split(/\r?\n/)) {
    if (!line.startsWith("data: ")) continue;
    const payload = line.slice(6).trim();
    if (!payload || payload === "[DONE]") continue;
    try {
      const d = JSON.parse(payload);
      if (d.token !== undefined) acc += d.token;
    } catch {
      acc += payload;
    }
  }
  return acc;
}

async function generateOne(prompt) {
  const res = await fetch(`${BASE}/api/generate`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      prompt: prompt.trim(),
      temperature: GEN.temperature,
      top_p: GEN.top_p,
      max_tokens: GEN.max_tokens,
    }),
  });
  if (!res.ok) {
    const err = await res.text();
    throw new Error(`HTTP ${res.status}: ${err}`);
  }
  const text = await res.text();
  return parseSseBody(text);
}

async function main() {
  const responses = [];
  for (let i = 0; i < PROMPTS.length; i++) {
    process.stderr.write(`[${i + 1}/${PROMPTS.length}] ${PROMPTS[i].slice(0, 50)}...\n`);
    responses.push(await generateOne(PROMPTS[i]));
  }
  const iso = new Date().toISOString();
  const body = responses.map((r) => `  ${JSON.stringify(r)},`).join("\n");
  const lines = [
    "/** Auto-generated by scripts/capture-evaluation.mjs — run again after model/backend changes. */",
    `export const EVALUATION_CAPTURED_AT_ISO = "${iso}";`,
    `export const CAPTURED_SAMPLING = { temperature: ${GEN.temperature}, top_p: ${GEN.top_p}, max_tokens: ${GEN.max_tokens} } as const;`,
    "",
    "export const CAPTURED_RESPONSES: readonly string[] = [",
    body,
    "];",
    "",
  ];
  writeFileSync(OUT, lines.join("\n"), "utf8");
  process.stderr.write(`Wrote ${OUT}\n`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
